public class House {

    //Instance var
    int numOfRooms;
    int numOfKithcens;
    int numOfBathrooms;
    boolean isStairs;

    House() {

    }

    House (int numOfRooms, int numOfKithcens, int numOfBathrooms, boolean isStairs) {
        this.numOfRooms = numOfRooms;
        this.numOfKithcens = numOfKithcens;
        this.numOfBathrooms = numOfBathrooms;
        this.isStairs = isStairs;
    }
}